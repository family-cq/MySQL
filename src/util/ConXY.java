package util;

public class ConXY {
    private int x;
    private int w;

    public ConXY(int x,int w){
        this.x = x;
        this.w = w;
    }

    public int getX() {
        return x;
    }

    public int getW() {
        return w;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setW(int w) {
        this.w = w;
    }
}
